public class IncrementoDecremento {
	public static void main(String[] args) {
		
		int contador = 5;

		// incremento
		contador++;     // Equivalente a contador = contador + 1;

		// decremento
		contador--;    // Equivalente a contador = contador - 1;

		System.out.println("Valor final do contador: " + contador);
	}
}